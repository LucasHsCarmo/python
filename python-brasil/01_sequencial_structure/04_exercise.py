"""
Faça um programa que peça as 4 notas bimestrais e mostre a média. 
"""

print("\n")
nt1 = float(input("Informe a 1º nota: "))
nt2 = float(input("Informe a 2º nota: "))
nt3 = float(input("Informe a 3º nota: "))
nt4 = float(input("Informe a 4º nota: "))

print("\nA média das notas é: {:.2f}".format((nt1+nt2+nt3+nt4)/4))